{
    m_pubSubChannelRequestsBeingProcessed.insert({makeChannelPrivateAndReturnStanzaId(channelJid), QXmppPubSubIq::QueryType::CreateQuery}, channelJid);
}

///
/// Transforms a private channel into a public one.
