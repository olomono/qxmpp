void QXmppMixManager::addMemberJid(const QString &channelJid, const QString &jid) const
{
    m_pubSubManager->publishItem(channelJid, ns_mix_node_allowed, QXmppPubSubItem(jid));
}

void QXmppMixManager::removeMemberJid(const QString &channelJid, const QString &jid) const
{
    m_pubSubManager->retractItem(channelJid, ns_mix_node_allowed, jid);
}

void QXmppMixManager::addBannedJid(const QString &channelJid, const QString &jid)
{
    m_pubSubChannelRequestsBeingProcessed.insert({m_pubSubManager->publishItem(channelJid, ns_mix_node_banned, QXmppPubSubItem(jid)), QXmppPubSubIq::QueryType::PublishQuery}, jid);
}

void QXmppMixManager::removeBannedJid(const QString &channelJid, const QString &jid) const
{
    m_pubSubManager->retractItem(channelJid, ns_mix_node_banned, jid);
}

