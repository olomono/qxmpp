{
    m_discoveryManager->requestItems(channelJid, "mix");
}

///
/// Subscribes to nodes of a channel.
///
/// \param channelJid JID of the channel with the passed nodes
/// \param nodes nodes to subscribe to
///
void QXmppMixManager::subscribeToNodes(const QString &channelJid, const QStringList &nodes)
{
    QXmppMixIq iq;

    iq.setType(QXmppIq::Set);
    iq.setTo(channelJid);
    iq.setActionType(QXmppMixIq::UpdateSubscription);
    iq.setNodesBeingSubscribed(nodes);

    client()->sendPacket(iq);
}

///
/// Unsubscribes from nodes of a channel.
///
/// \param channelJid JID of the channel with the passed nodes
/// \param nodes nodes to subscribe to
///
void QXmppMixManager::unsubscribeFromNodes(const QString &channelJid, const QStringList &nodes)
{
    QXmppMixIq iq;

    iq.setType(QXmppIq::Set);
    iq.setTo(channelJid);
    iq.setActionType(QXmppMixIq::UpdateSubscription);
    iq.setNodesBeingUnsubscribed(nodes);

    client()->sendPacket(iq);
}

