///
/// Creates a channel.
///
/// \param channelOrServiceJid JID of the MIX channel (e.g. "channel@mix.example.org") or only the JID of the MIX service (e.g. "mix.example.org") to create a channel with an ID generated by the server (e.g. "09eb3353-a414-4a41-99b9-c123824b1aea@mix.example.org")
/// \param isPublic true for creating a public group chat or false (default) for a private one; this parameter is not relevant if channelOrServiceJid consists only of the service JID because the channel being created then will not be publicly accessible anyway
///
void QXmppMixManager::createChannel(const QString &channelOrServiceJid, const bool isPublic)
{
    QXmppMixIq iq;

    iq.setType(QXmppIq::Set);
    iq.setTo(QXmppUtils::jidToDomain(channelOrServiceJid));
    iq.setActionType(QXmppMixIq::Create);
    iq.setChannelId(QXmppUtils::jidToUser(channelOrServiceJid));

    // Cache the request for a private channel to create the corresponding "Allowed" node after channel creation.
    // TODO: The current XEP seems to tend to not create an Allowed node if private without a channel ID by "if (!isPublic && !iq.channelId().isEmpty())"
    // but the problem is that everyone who knows the channel JID can join it at the moment because it cannot be "not addressable" as specified in the XEP or the question is what they mean by that
    if (!isPublic || iq.channelId().isEmpty())
        m_requestsBeingProcessed.insert(iq.id(), channelOrServiceJid);

    client()->sendPacket(iq);
}

///
/// Joins a channel to become a participant of it.
///
/// \param channelJid JID of the channel being joined
/// \param nick nickname of the user (by default, required by the server) or none if no nickname should be set
/// \param subscribeToMemberJids nothing or true for being informed about added and removed member JIDs, otherwise false
/// \param subscribeToBannedJids nothing or true for being informed about added and removed banned JIDs, otherwise false
/// \param subscribeToConfiguration nothing or true for being informed about configuration changes, otherwise false
/// \param subscribeToInformation nothing or true for being informed about information changes, otherwise false
/// \param subscribeToMessages nothing or true for receiving messages which were sent over the channel, otherwise false
/// \param subscribeToParticipants nothing or true for being informed about joined users and left participants, otherwise false
/// \param subscribeToPresences nothing or true for being informed about the participants' presences, otherwise false
///
void QXmppMixManager::joinChannel(const QString &channelJid, const QString &nick, const bool subscribeToMemberJids, const bool subscribeToBannedJids, const bool subscribeToConfiguration, const bool subscribeToInformation, const bool subscribeToMessages, const bool subscribeToParticipants, const bool subscribeToPresences)
{
    QXmppMixIq iq;

    iq.setType(QXmppIq::Set);
    iq.setTo(client()->configuration().jidBare());
    iq.setActionType(QXmppMixIq::ClientJoin);
    iq.setChannelJid(channelJid);

    if (!nick.isEmpty())
        iq.setNick(nick);

    QStringList nodesBeingSubscribed;

    if (subscribeToMemberJids)
        nodesBeingSubscribed.append(ns_mix_node_allowed);
    if (subscribeToBannedJids)
        nodesBeingSubscribed.append(ns_mix_node_banned);
    if (subscribeToConfiguration)
        nodesBeingSubscribed.append(ns_mix_node_config);
    if (subscribeToInformation)
        nodesBeingSubscribed.append(ns_mix_node_info);
    if (subscribeToMessages)
        nodesBeingSubscribed.append(ns_mix_node_messages);
    if (subscribeToParticipants)
        nodesBeingSubscribed.append(ns_mix_node_participants);
    if (subscribeToPresences)
        nodesBeingSubscribed.append(ns_mix_node_presence);

    iq.setNodesBeingSubscribed(nodesBeingSubscribed);

    // Cache the request to retrieve the channel JID for emitting  a corresponding signal.
    m_requestsBeingProcessed.insert(iq.id(), channelJid);

    client()->sendPacket(iq);
}

